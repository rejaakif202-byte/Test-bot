ALPHABET_LOWER = "abcdefghijklmnopqrstuvwxyz"
ALPHABET_UPPER = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"

def convert_font(text: str, font_name: str) -> str:
    font = FONTS.get(font_name)
    if not font:
        return text
    result = []
    lower_map = font.get("lower", "")
    upper_map = font.get("upper", "")
    for char in text:
        if char.lower() in ALPHABET_LOWER:
            idx = ALPHABET_LOWER.index(char.lower())
            if char.isupper() and idx < len(upper_map):
                mapped = list(upper_map)
                result.append(mapped[idx] if idx < len(mapped) else char)
            elif idx < len(lower_map):
                mapped = list(lower_map)
                result.append(mapped[idx] if idx < len(mapped) else char)
            else:
                result.append(char)
        else:
            result.append(char)
    return "".join(result)

def get_font_names():
    return list(FONTS.keys())
